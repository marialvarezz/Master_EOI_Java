package Ejercicios;

public class Alumno {
    String nombre;
    String apellido;
    String email;
    String movil;

    public Alumno(String nombre, String apellido, String email, String movil) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.email = email;
        this.movil = movil;
    }
}
